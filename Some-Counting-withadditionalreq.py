# Exercise 7: Some Counting - 20 Marks

for i in range (0, 51): # Print a count from 0 to 50
    print(i) # Display it to the user
    
for i in range (50, -1, -1): # Count 50 to 0 in increments of -1 so it displays at 0.
    print(i)
    
for i in range(30, 51): # Prints 30 to 50
    print(i)
    
for i in range(50, 11, -2): # Count down from 50 to 10 in -2 so it goes down by 2 and displays a 10 by the end of it.
    print(i)
    
for i in range(100, 201, 5): # Count from 100 to 200 in incriments of 5
    print(i)